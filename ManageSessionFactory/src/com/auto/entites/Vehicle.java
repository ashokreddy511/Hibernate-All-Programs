package com.auto.entites;

import java.util.Date;

public class Vehicle {
	private int vehicleNo;
	private String vehicleModelNO;
	private String color;
	private String vehiclePurchaseDate;
	private String chasisNO;
	private String engineNo;
	private String registrationNo;
	private String batteryNo;
	public int getVehicleNo() {
		return vehicleNo;
	}
	public void setVehicleNo(int vehicleNo) {
		this.vehicleNo = vehicleNo;
	}
	public String getVehicleModelNO() {
		return vehicleModelNO;
	}
	public void setVehicleModelNO(String vehicleModelNO) {
		this.vehicleModelNO = vehicleModelNO;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getVehiclePurchaseDate() {
		return vehiclePurchaseDate;
	}
	public void setVehiclePurchaseDate(String vehiclePurchaseDate) {
		this.vehiclePurchaseDate = vehiclePurchaseDate;
	}
	public String getChasisNO() {
		return chasisNO;
	}
	public void setChasisNO(String chasisNO) {
		this.chasisNO = chasisNO;
	}
	public String getEngineNo() {
		return engineNo;
	}
	public void setEngineNo(String engineNo) {
		this.engineNo = engineNo;
	}
	public String getRegistrationNo() {
		return registrationNo;
	}
	public void setRegistrationNo(String registrationNo) {
		this.registrationNo = registrationNo;
	}
	public String getBatteryNo() {
		return batteryNo;
	}
	public void setBatteryNo(String batteryNo) {
		this.batteryNo = batteryNo;
	}
	

}
